                <h2>About the Collection</h2>
                <p>John Peel's collection contains his favourite records from a career spanning nearly 40 years of opening ears. From the 60s to the 00s, these records aired on John's Radio London and BBC shows often provided the first plays for bands - many of whom would go on to achieve great fame.</p>
                <p>With so few DJs having the freedom of their own playlist or appetite to experiment so widely, it was frequently John who introduced us to music that otherwise would have had no outlet in the mainstream. Consequently many of the sea-change moments in music - from Psychadelia to Reggae, Electronica, Ska, Punk, New-wave, Hip-hop, Trance - were brought to the attention of the UK public via these very pieces of plastic.</p>
                <h2>About John</h2>
                <p>Early championing of <a href="/pink-floyd">Pink Floyd</a>, <a href="/david-bowie">David Bowie</a>, The Specials, <a href="/neu">Neu</a>, Kraftwerk, The Smiths, Pulp, etc, etc, make these records both the source of - and an enduring chronological catalog of - one man's tastes that became a nation's, leaving an indelible mark on the culture of our time.</p>
                <h2>The John Peel Archive - a History Of Music, Society and Culture</h2>
                <p>We are currently working towards making John's collection available to the public, so that you can explore it to your heart's content. Ultimately, we'd like to:</p>
                <ul>
                    <li>Enable exploration (& especially serendipitous discovery)</li>
                    <li>Listen, see, "smell" the music and how it came about</li>
                    <li>Curated by John, explained by the people who were there</li>
                </ul>
